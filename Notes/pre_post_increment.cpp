#include <iostream>
using namespace std;

int main(){
    int a = 0;
    a++;                                //increases "a" by 1
    ++a;                                //increases "a" by 1

    a--;                                //decreases "a" by 1
    --a;                                //decreases "a" by 1

    a += 3;                             // a = a + 3

    cout << "a = " << a <<endl;
}